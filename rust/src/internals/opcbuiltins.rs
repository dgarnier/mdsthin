pub fn opcode_to_num(opcode: &str) -> Option<u16> {
    OPCODE_MAP.get(opcode).copied()
}

//  don't use this function, it is not efficient
pub fn num_to_opcode(num: u16) -> Option<&'static str> {
    OPCODE_MAP.iter().find_map(|(k, &v)| if v == num { Some(k) } else { None })
}

macro_rules! args_repr {
    ($args:expr) => {
        $args.iter().map(|a| format!("{:?}", a)).collect::<Vec<_>>().join(", ")
    };
}

macro_rules! opcode_func_repr {
    ($opname:literal, $args:expr) => {
        format!("$opname({})", args_repr!($args))
    };
}

